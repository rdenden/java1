public class Hero {
    private String name;
    private int enemy;
    public Hero(String name) {
        this.name = name;
    }
    public String getName() {
        return this.name;
    }
    public void setEnemy(int enemy) {
        this.enemy = enemy; 
    }
    public int getEnemy() {
        return this.enemy;
    }
    
}